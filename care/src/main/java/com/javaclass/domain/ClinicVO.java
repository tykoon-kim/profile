package com.javaclass.domain;

public class ClinicVO {

	
		private int cliniccode;
		private String clinicname;
		private String clinicaddr;
		private String clinictel;
		private int regioncode;
		private String clinicimg;
		
		public String getClinicimg() {
			return clinicimg;
		}
		public void setClinicimg(String clinicimg) {
			this.clinicimg = clinicimg;
		}
		public int getCliniccode() {
			return cliniccode;
		}
		public void setCliniccode(int cliniccode) {
			this.cliniccode = cliniccode;
		}
		public String getClinicname() {
			return clinicname;
		}
		public void setClinicname(String clinicname) {
			this.clinicname = clinicname;
		}
		public String getClinicaddr() {
			return clinicaddr;
		}
		public void setClinicaddr(String clinicaddr) {
			this.clinicaddr = clinicaddr;
		}
		public String getClinictel() {
			return clinictel;
		}
		public void setClinictel(String clinictel) {
			this.clinictel = clinictel;
		}
		public int getRegioncode() {
			return regioncode;
		}
		public void setRegioncode(int regioncode) {
			this.regioncode = regioncode;
		}
		
}
