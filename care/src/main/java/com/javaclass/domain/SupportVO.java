package com.javaclass.domain;


public class SupportVO {
	
	private int supcode;
	private String supname;	
	private String supcontent;	
	private String supcheck;	
	private String supurl;
	private String supage;
	private String suppoto;
	
	
	public int getSupcode() {
		return supcode;
	}
	public void setSupcode(int supcode) {
		this.supcode = supcode;
	}
	public String getSupname() {
		return supname;
	}
	public void setSupname(String supname) {
		this.supname = supname;
	}
	public String getSupcontent() {
		return supcontent;
	}
	public void setSupcontent(String supcontent) {
		this.supcontent = supcontent;
	}

	public String getSupcheck() {
		return supcheck;
	}
	public void setSupcheck(String supcheck) {
		this.supcheck = supcheck;
	}

	public String getSupurl() {
		return supurl;
	}
	public void setSupurl(String supurl) {
		this.supurl = supurl;
	}
	public String getSupage() {
		return supage;
	}
	public void setSupage(String supage) {
		this.supage = supage;
	}
	public String getSuppoto() {
		return suppoto;
	}
	public void setSuppoto(String suppoto) {
		this.suppoto = suppoto;
	}
	
}
